export function getPlaceCategory(type = '') {
  const value = type.toLowerCase()
  if (/beach|coastal/.test(value)) return 'beaches'
  if (/wildlife|forest|national park|tiger|safari|sanctuary/.test(value)) return 'wildlife'
  if (/temple|monastery|cathedral|spiritual|pilgrimage|buddhist/.test(value)) return 'spiritual'
  if (/museum|institute|science|aquarium|planetarium|craft centre/.test(value)) return 'museums'
  if (/park|garden|recreation|amusement|lake/.test(value)) return 'recreation'
  if (/viewpoint|waterfall|river|nature|village|valley|tea|mountain/.test(value)) return 'nature'
  return 'heritage'
}
